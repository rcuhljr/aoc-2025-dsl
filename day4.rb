def count_neighbors(grid, x, y)
  count = 0
  (-1..1).each do |x_off|
    (-1..1).each do |y_off|
      next if x_off == 0 && y_off == 0
      tempx = x + x_off
      tempy = y + y_off
      next if tempx < 0 || tempy < 0
      next if tempx >= grid[0].size || tempy >= grid.size
      count += 1 if grid[tempy][tempx]
    end
  end
  count
end


def solveA(input)
  layout = input.split().map{|row| row.each_char.map{|x| x == '@' }}
  count = 0
  (0..layout.size-1).each do |y|
    (0..layout[0].size-1).each do |x|
      next unless layout[y][x]
      if count_neighbors(layout, x, y) < 4
        # puts "(#{x},#{y})"
        count += 1
      end
    end
  end
  count
end

def solveB(input)
  layout = input.split().map{|row| row.each_char.map{|x| x == '@' }}
  count = 0
  has_changed = true
  while has_changed do
    has_changed = false
    (0..layout.size-1).each do |y|
      (0..layout[0].size-1).each do |x|
        next unless layout[y][x]
        if count_neighbors(layout, x, y) < 4
          has_changed = true
          layout[y][x] = false
          count += 1
        end
      end
    end
  end
  count
end




